num1 = 20
num2 = -20

#if statement
if num1 != num2:
    print('numbers are not equal!')

#if-else statement
if num1 != num2:
    print('numbers are not equal!')
else:
    print('Numbers are equal!')

#if-elif statement
if num1 > 0 and num2 > 0:
    print('Positive numbers!')
elif num1 < 0 and num2 < 0:
    print('Negative numbers!')
else:
    print('Positive and negative numbers!')